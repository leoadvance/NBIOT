/*******************************************************************************
*							陆超@2017-04-16
* Function Name  :	OLED_String_6x8
* Description	 :	OLED 显示6 * 8 ascii
* Input 		 :	u8 x		x轴 0~127
*					u8 y    	y轴 0~64
*					u8 *pData   要显示的字符串
*					u8 ucLen	要写的长度
* Output		 :	None
* Return		 :	None
*******************************************************************************/
void OLED_String_6x8(u8 x, u8 y, u8 *pData, u8 ucLen)
{
	u8 i;
	u8 *pF6x8;
	
	// 是否还有字节
	while (ucLen--)
	{    
		// 定位到镜像表
		pF6x8 = (u8 *)&F6x8[((*pData) - 32) * 6];

		// 是否跨行
		if (x > X_WIDTH - 6)
		{
			x  = 0;
			y += 8;
		} 

		// 修改数字
		for(i = 0; i < 6; i++) 
		{
			OLED_Draw_YPage(x + i, y, &pF6x8[i], 1);
		}

		// 下一个数据
		x += 6;
		pData++;
	}
	
}// End of void OLED_String_6x8(u8 x, u8 y, u8 *pData, u8 ucLen

/*******************************************************************************
*							陆超@2017-04-16
* Function Name  :	OLED_Draw_YPage
* Description	 :	OLED 写y轴
* Input 		 :	u8 x		x轴 0~127
*					u8 y    	y轴 0~64
*					u8 *pData   要写入的数组
*					u8 ucByte   要写入的字节
* Output		 :	None
* Return		 :	None
*******************************************************************************/
void OLED_Draw_YPage(u8 x, u8 y, u8 *pData, u8 ucByte)
{
	u8 i, Remainder, y_Page;

	u8 ucAND_Sign;

	// 越界保护
	if (x >= X_WIDTH)
	{
		return;
	}
	if (y >= Y_WIDTH)
	{
		return;
	}
	
	// 是否整字节
	Remainder = y % 8;
	if (Remainder != 0)
	{
		// 获取与标签
		ucAND_Sign       = 0xFF << (8 - Remainder);
		ucAND_Sign     >>= 8 - Remainder;

		y_Page = y / 8;
		for (i = 0; i < ucByte; i++)
		{
			// 越界
			if (y_Page + i >= Y_PAGE - 1)
			{
				return;
			}

			// 分别保存上下部分
			OLED_Map[x][y_Page + i]     &= ucAND_Sign;
			OLED_Map[x][y_Page + i]     |= (*pData) << Remainder;
			OLED_Map[x][y_Page + i + 1] &= ~ucAND_Sign;
			OLED_Map[x][y_Page + i + 1] |= (*pData) >> (8 - Remainder);

			// 写入数据
			OLED_Set_Pos(x, y + (i * 8));
			OLED_Write_Data(OLED_Map[x][y_Page + i]);
			OLED_Set_Pos(x, y + ((i + 1) * 8));
			OLED_Write_Data(OLED_Map[x][y_Page + (i + 1)]);
		}

	}
	else
	{
		
		y_Page = y / 8;
		for (i = 0; i < ucByte; i++)
		{
			// 越界
			if (y_Page + i >= Y_PAGE)
			{
				return;
			}

			
			// 修改数据
			OLED_Map[x][y_Page + i] = *pData++;

			// 设置修改地址
			OLED_Set_Pos(x, y + (i * 8));
			OLED_Write_Data(OLED_Map[x][y_Page + i]);
		}		
	}

}// End of void OLED_Draw_YPage(u8 x, u8 y, u8 *pData, u8 ucByte)



