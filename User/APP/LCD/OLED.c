/*******************************************************************************
*                           陆超@2017-04-10
* Function Name  :  OLED_Write_Data
* Description    :  OLED写数据
* Input          :  u8 ucData 要写入的数据
* Output         :  None
* Return         :  None
*******************************************************************************/
void OLED_Write_Data(u8 ucData)
{
	u8 i = 8;

	OLED_DC_SET();
	OLED_SCL_CLR();

	while(i--)
	{
		if(ucData & 0x80)
		{
			OLED_SDA_SET();
		}
		else
		{
			OLED_SDA_CLR();
		}

		OLED_SCL_SET(); 
		          
		OLED_SCL_CLR();    
		ucData <<= 1;    
	}
	
}// End of void OLED_Write_Data(u8 ucData)

/*******************************************************************************
*                           陆超@2017-04-10
* Function Name  :  OLED_Write_Cmd
* Description    :  OLED写命令
* Input          :  u8 ucCmd 要写入的命令
* Output         :  None
* Return         :  None
*******************************************************************************/
void OLED_Write_Cmd(u8 ucCmd)
{
	u8 i=8;


	OLED_DC_CLR();
	OLED_SCL_CLR();

	while(i--)
	{
		if(ucCmd & 0x80)
		{
			OLED_SDA_SET();
		}
		else
		{
			OLED_SDA_CLR();
		}

		OLED_SCL_SET();

		OLED_SCL_CLR();    
		ucCmd <<= 1;;   
	} 	
	
}// End of void OLED_Write_Cmd(u8 ucCmd)

/*******************************************************************************
*							陆超@2017-04-12
* Function Name  :	OLED_Set_Pos
* Description	 :	OLED设置位置
* Input 		 :	u8 x x坐标 0~128
*					u8 y y坐标 0~63 必须是8的倍数
* Output		 :	None
* Return		 :	None
*******************************************************************************/
void OLED_Set_Pos(u8 x, u8 y)
{ 
	// 设置地址y 0~7
	OLED_Write_Cmd(OLED_CMD_Y_ADDR + (y / 8));

	// 设置地址x低字节和高字节
	OLED_Write_Cmd(( x & 0x0F));
	OLED_Write_Cmd(((x & 0xF0) >> 4) | 0x10);
	
}// End of void OLED_Set_Pos(u8 x, u8 y) 

/*******************************************************************************
*							陆超@2017-04-13
* Function Name  :	OLED_Fill
* Description	 :	OLED数据填充
* Input 		 :	u8 Data	待填充数据
* Output		 :	None
* Return		 :	None
*******************************************************************************/
void OLED_Fill(u8 Data)
{
    u16 y = 0, x = 0;
	
	for (y = 0; y < Y_PAGE; y++)
	{
		OLED_Set_Pos(0, y << 3);
		for (x = 0; x < X_WIDTH; x++)
		{
			OLED_Map[x][y] = Data;
			OLED_Write_Data(OLED_Map[x][y]);
		}
	}


}// End of void OLED_Fill(u8 *pData)

